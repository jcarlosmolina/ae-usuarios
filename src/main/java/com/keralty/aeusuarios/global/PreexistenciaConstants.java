    /**
     * Method that returns the identification fields in this table.
     * - If className is 'Preexistencia', it returns the identification keys
     * - If className is a facet of the inheritance net of 'Preexistencia', it returns the foreign keys to the table of the facet className
     * - Otherwise it returns an empty array
     * @param className Facet whose keys are to be returned
     * @return Keys in the table for facet className
     */
    public static final List<String> getIdFields(String className) {
        List<String> returnList = new ArrayList<>();
        if (className == null || className.isEmpty()){
            returnList.add(ROLE_NAME_USUARIO + "." + UsuarioConstants.ROLE_NAME_SOLICITUD + "." + SolicitudConstants.ROLE_NAME_AESOLICITUDES + "." + AESOLICITUDESConstants.ATTR_FIELD_AESOLICITUDESIDSOLICITUD);
            returnList.add(ROLE_NAME_USUARIO + "." + UsuarioConstants.ATTR_FIELD_USUARIOIDENTIFICADOR);
            returnList.add(ATTR_FIELD_PREEXISTENCIAIDENTIFICADOR);
        }
        // Facet 'Preexistencia' (This facet)
        if (PreexistenciaConstants.CLASS_NAME.equalsIgnoreCase(className)) {
            returnList.add(ROLE_NAME_USUARIO + "." + UsuarioConstants.ROLE_NAME_SOLICITUD + "." + SolicitudConstants.ROLE_NAME_AESOLICITUDES + "." + AESOLICITUDESConstants.ATTR_FIELD_AESOLICITUDESIDSOLICITUD);
            returnList.add(ROLE_NAME_USUARIO + "." + UsuarioConstants.ATTR_FIELD_USUARIOIDENTIFICADOR);
            returnList.add(ATTR_FIELD_PREEXISTENCIAIDENTIFICADOR);
        }
        return returnList;
    }
